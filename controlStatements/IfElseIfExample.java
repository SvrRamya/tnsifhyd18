package controlStatements;

public class IfElseIfExample {
	public static void main(String[] args) {  
	    int num=-79;  
	      
	    if(num<0){  
	        System.out.println("Negative");  
	    }  
	    else if(num>0){  
	        System.out.println("Positive");  
	    }  
	    else{  
	        System.out.println("Neither Negative nor Positive");  
	    }  
	}  


}
